#include <stdio.h>

int main(void) {
  int h, m, s;
  char a[9], b[9];

  scanf("%s", a);
  scanf("%s", b);

  h = (b[0]-'0')*10 + (b[1]-'0') - (a[0]-'0')*10 - (a[1]-'0');
  m = (b[3]-'0')*10 + (b[4]-'0') - (a[3]-'0')*10 - (a[4]-'0');
  s = (b[6]-'0')*10 + (b[7]-'0') - (a[6]-'0')*10 - (a[7]-'0');

  if (s < 0) s += 60, m--;
  if (m < 0) m += 60, h--;
  if (h < 0) h += 24;

  printf("%02d:%02d:%02d", h, m, s);
  return 0;
}